<h2>Events </h2> 
<p>
Events specifies the date and how many events are being hosted on that day, which are automatically updated once a Venue or Promoter uploades a new event.
</p>
<h2>Venues</h2>
<p>
Once selected the user can select the Venue of their choice and browse through events on screen for that specific Venue from closest date forward.
<p/>
<h2>Prefrences</h2>
<p>
Prefrences is the final section, it is made of up differene genres.<br />
There is a list of genres ranging from Chart music all the way to World music. <br />
